import React, {useState} from 'react';
import {
  StyleSheet,
  Text,
  View,
  TouchableOpacity,
  Button,
  Image,
  PermissionsAndroid,
} from 'react-native';
import * as ImagePicker from 'react-native-image-picker';

const Camera = () => {
  const [bs64, setbs64] = useState(null);
  pickImage = () => {
    let options = {
      storageOptions: {
        //   skipBackup: true,

        maxWidth: 300,
        maxHeight: 550,
        quality: 1,
      },
      // chyển anhe thành base64
      includeBase64: true,
    };
    ImagePicker.launchImageLibrary(options, response => {
      if (response.didCancel) {
        console.log('User cancelled image picker');
      } else if (response.error) {
        console.log('ImagePicker Error: ', response.error);
      } else if (response.customButton) {
        console.log('User tapped custom button: ', response.customButton);
        alert(response.customButton);
      } else {
        const source = {uri: response.uri};

        console.log('response', JSON.stringify(response));

        console.log('uri => ', response.assets[0].uri);
        console.log('width => ', response.assets[0].width);
        console.log('height => ', response.assets[0].height);
        console.log('fileSize => ', response.assets[0].fileSize);
        console.log('type => ', response.assets[0].type);
        console.log('fileName => ', response.assets[0].fileName);
        let source1 = response.assets[0].uri;
        let source2 = response.assets[0].base64;
        console.log('base64 => ', source2);
        setbs64(source2);
      }
    });
  };

  // kien tra quyen==============================================
  const requestCameraPermission = async () => {
    try {
      const granted = await PermissionsAndroid.request(
        PermissionsAndroid.PERMISSIONS.CAMERA,
      );
      const granted1 = await PermissionsAndroid.request(
        PermissionsAndroid.PERMISSIONS.WRITE_EXTERNAL_STORAGE,
      );
      if (granted === PermissionsAndroid.RESULTS.GRANTED) {
        console.log('You can use the camera');
      } else {
        console.log('Camera permission denied');
      }
      if (granted1 === PermissionsAndroid.RESULTS.GRANTED) {
        console.log('You can use the camera1');
      } else {
        console.log('Camera permission denied1');
      }
    } catch (err) {
      console.warn(err);
    }
  };
  // kien tra quyen=====()=========================================

  return (
    <View style={styles.container}>
      <View style={styles.container}>
        {bs64 && (
          <Image
            source={{
              uri: 'data:image/jpeg;base64,' + bs64,
              //uri: bs64,
            }}
            style={{width: 100, height: 100}}
          />
        )}
        <TouchableOpacity
          onPress={requestCameraPermission}
          style={styles.button}>
          <Text style={styles.buttonText}>chonj quyen</Text>
        </TouchableOpacity>
        <TouchableOpacity onPress={pickImage} style={styles.button}>
          <Text style={styles.buttonText}>chonj anh</Text>
        </TouchableOpacity>
      </View>
    </View>
  );
};

export default Camera;

